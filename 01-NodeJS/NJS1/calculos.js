//estas cuatro funciones suman-restan-multiplican-y dividen cada una de estas funciones exportan 
export function suma(n1 ,n2){
    return n1+n2;
};

export function resta(n1,n2){
    return n1-n2;
};

export function multiplicar(n1,n2){
    return n1*n2;
};

export function dividir(n1,n2){
    return n1/n2;
};