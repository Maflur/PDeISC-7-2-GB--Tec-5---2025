//esta es la funcion de sumar 
function suma(n1 ,n2){
    return n1+n2;
};
//esta es la funcion de resta
 function resta(n1,n2){
    return n1-n2;
};
//esta es la funcion de multiplicar
function multiplicar(n1,n2){
    return n1*n2;
};
//esta es la funcion de divir
function dividir(n1,n2){
    return n1/n2;
};
//aca se muestra por consola las anteriores funciones
console.log(suma(1,8));
console.log(resta(7,5));
console.log(multiplicar(2,5));
console.log(dividir(25,5));